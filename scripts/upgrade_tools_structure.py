def replace_tools_section(content, scenario_key, new_tools_str):
    """替换指定场景的 tools 部分"""
    # 找到场景的起点
    scenario_start_marker = f'"{scenario_key}": {{'
    start_idx = content.find(scenario_start_marker)
    if start_idx == -1:
        print(f"警告：未找到场景 {scenario_key}")
        return content
    
    # 找到 tools: { 的位置
    tools_start_marker = '"tools": {'
    tools_start = content.find(tools_start_marker, start_idx)
    if tools_start == -1:
        print(f"警告：在场景 {scenario_key} 中未找到 tools")
        return content
    
    # 找到 tools 对应的闭合 }
    # 从 tools: { 的 { 开始，找到匹配的 }
    brace_count = 0
    i = tools_start + len('"tools":')
    
    # 跳过空白字符找到第一个 {
    while i < len(content) and content[i] in ' \t\n\r':
        i += 1
    
    if content[i] != '{':
        print(f"警告：tools 后不是 {{" )
        return content
    
    brace_count = 1
    i += 1
    
    while i < len(content) and brace_count > 0:
        if content[i] == '{':
            brace_count += 1
        elif content[i] == '}':
            brace_count -= 1
        i += 1
    
    tools_end = i  # 指向闭合 } 之后
    
    # 找到 tools 后面的逗号（如果有）
    # 实际上我们替换整个 "tools": { ... } 结构
    # 包括后面的逗号和换行
    while i < len(content) and content[i] in ', \t':
        i += 1
    # 包含换行
    if i < len(content) and content[i] == '\n':
        i += 1
    
    # 执行替换
    new_content = content[:tools_start] + new_tools_str + content[i:]
    
    print(f"  ✓ 场景 {scenario_key} 的 tools 已替换")
    return new_content


